-- Inventory: sections + items (product + size), in-stock / to-order counts.
CREATE TABLE inventory_sections (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT NOT NULL,
  sort_order INTEGER NOT NULL,
  updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE inventory_items (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  section_id INTEGER NOT NULL REFERENCES inventory_sections(id),
  name TEXT NOT NULL,
  size TEXT,
  in_stock INTEGER NOT NULL DEFAULT 0 CHECK (in_stock >= 0),
  to_order INTEGER NOT NULL DEFAULT 0 CHECK (to_order >= 0),
  sort_order INTEGER NOT NULL,
  updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX inventory_items_section_idx ON inventory_items(section_id, sort_order);

